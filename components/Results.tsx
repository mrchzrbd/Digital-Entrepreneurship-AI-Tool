function exportInsights(results: AnalysisResult) {
  const date = new Date().toLocaleDateString();
  const lines = [
    '============================',
    '  AI ENTREPRENEUR COPILOT',
    `  Analysis — ${date}`,
    '============================',
    '',
    '🔑 TOP SIGNAL',
    results.topSignal,
    '',
    '📋 SUMMARY',
    results.summary,
    '',
    '💡 KEY INSIGHTS',
    ...results.insights.map((i, idx) => `${idx + 1}. ${i.title}\n   ${i.detail}\n   Frequency: ${i.frequency}`),
    '',
    '⚡ PRIORITIES',
    ...results.priorities.map((p) => `${p.rank}. [${p.urgency.toUpperCase()}] ${p.item}\n   ${p.impact}`),
    '',
    '🎯 RECOMMENDED ACTIONS',
    ...results.actions.map((a, idx) => `${idx + 1}. ${a.action}\n   Why: ${a.why}\n   When: ${a.timeframe}`),
    '',
    '============================',
    'Generated by AI Entrepreneur Copilot',
  ];

  const blob = new Blob([lines.join('\n')], { type: 'text/plain' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `copilot-insights-${date.replace(/\//g, '-')}.txt`;
  a.click();
  URL.revokeObjectURL(url);
}

